    private void btnAdicionarSetorjButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarSetorjButton6ActionPerformed
        SetorBEAN setor = new SetorBEAN();
        boolean campoVazio = this.conferirCampoVazio();
        if (campoVazio == true) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Nome para concluir o cadastro!");
        } else {
            setor.setNome(tfNomeSetor.getText());
            setor.setDescricao(txDescricaoSetor.getText());

            String setorValido = controleSetor.cadastrarSetor(setor);
            arrayDadosTabelaSetores = controleSetor.verTodosSetores();
            this.preencherTabelaSetores(arrayDadosTabelaSetores);
            JOptionPane.showMessageDialog(null, setorValido);

            this.limparCampos();
        }
    }//GEN-LAST:event_btnAdicionarSetorjButton6ActionPerformed

    private void btnEditarSetorjButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarSetorjButton7ActionPerformed
        boolean campoVazio = this.conferirCampoVazio();
        SetorBEAN setor = new SetorBEAN();
        if (campoVazio == true) {
            JOptionPane.showMessageDialog(null, "Para editar localize um setor primeiro.");
        } else {
            setor.setCod(Integer.parseInt(lblCodigo.getText()));
            setor.setNome(tfNomeSetor.getText());
            setor.setDescricao(txDescricaoSetor.getText());
            int i = JOptionPane.showConfirmDialog(null, "Deseja realmente editar o setor: " + setor.getNome());
            if (i == 0) {
                boolean aux = controleSetor.editarSetor(setor);
                if (aux == true) {
                    JOptionPane.showMessageDialog(null, "Setor editado com sucesso!");
                    arrayDadosTabelaSetores = controleSetor.verTodosSetores();
                    this.preencherTabelaSetores(arrayDadosTabelaSetores);
                } else {
                    JOptionPane.showMessageDialog(null, "Setor ja esta cadastrada ha um usuário, não pode ser excluido!");
                }
            }
            this.limparCampos();;
        }
    }//GEN-LAST:event_btnEditarSetorjButton7ActionPerformed

    private void btnExcluirSetorjButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirSetorjButton8ActionPerformed
        boolean camposVazios = this.conferirCampoVazio();
        if (camposVazios == true) {
            JOptionPane.showMessageDialog(null, "Para excluir, localize um setor primeiro.");
        } else {
            int cod = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o setor '" + tfNomeSetor.getText() + "' ?");
            if (cod == 0) {
                boolean usuarioExcluido = controleSetor.removerSetor(Integer.parseInt(lblCodigo.getText()));
                if (usuarioExcluido == true) {

                    JOptionPane.showMessageDialog(null, "Setor excluído com sucesso.");
                    arrayDadosTabelaSetores = controleSetor.verTodosSetores();
                    this.preencherTabelaSetores(arrayDadosTabelaSetores);
                    this.limparCampos();
                } else {
                    JOptionPane.showMessageDialog(null, "O setor tem um usuario cadastrado ha ele. não poe ser excluido!. ");
                    this.limparCampos();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Exclusão não efetivada. ");
            }
        }
    }//GEN-LAST:event_btnExcluirSetorjButton8ActionPerformed

