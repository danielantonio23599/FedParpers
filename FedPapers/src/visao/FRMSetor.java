    private void jButton40ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton40ActionPerformed
        String entrada = JOptionPane.showInputDialog("Digite o código do setor");
        if (!entrada.equals("")) {
            ArrayList<SetorBEAN> arrayAux = controleSetor.localizarSetor(Integer.parseInt(entrada));
            this.preencherTabelaSetores(arrayAux);
            if (arrayAux.size() > 0) {
                for (SetorBEAN objSetor : arrayAux) {
                    lblCodigo.setText("" + objSetor.getCod());
                    tfNomeSetor.setText(objSetor.getNome());
                    txDescricaoSetor.setText(objSetor.getDescricao());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Setor não encontrado!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campo vasio!");
        }
    }//GEN-LAST:event_jButton40ActionPerformed

