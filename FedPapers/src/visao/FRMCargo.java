    private void tableCargosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCargosMouseClicked
        if (tableCargos.getRowCount() == 1) {
            ArrayList<CargoBEAN> aux = ca.localizar(Integer.parseInt((tableCargos.getValueAt(0, 0)) + ""));
            this.pegaSelecionado(aux);
        } else {
            ArrayList<CargoBEAN> aux = ca.listarALL();
            this.pegaSelecionado(aux);
        }
    }//GEN-LAST:event_tableCargosMouseClicked

