    private void btnAdicionarjButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarjButton6ActionPerformed
        CargoBEAN c = new CargoBEAN();
        if (!tfNome.getText().equals("")) {
            if (taDescricao.getText().equals("")) {
                taDescricao.setText(null);
            }
            System.out.println(tfNome.getText());
            c.setNome(tfNome.getText());
            c.setDescricao(taDescricao.getText());
            boolean aux = ca.adicionar(c);
            if (aux == true) {
                JOptionPane.showMessageDialog(null, "Cargo cadastrado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "O " + c.getNome() + " já esta cadastrado!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Prencha o nome");
        }
        dados = ca.listarALL();
        this.preencheTabela(dados);
        this.limparCampos();
    }//GEN-LAST:event_btnAdicionarjButton6ActionPerformed

    private void btnEditarjButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarjButton7ActionPerformed
        boolean v = this.verificaCampos();
        if (v == true) {
            int opc = JOptionPane.showConfirmDialog(null, "Deseja realmente editar o cargo de ");
            if (opc == 0) {
                CargoBEAN c = this.pegarCampos();
                int i = JOptionPane.showConfirmDialog(null, "Deseja realmente editar o cargo :" + c.getNome());
                if (i == 0) {
                    ca.editar(c);
                    JOptionPane.showMessageDialog(null, "Os dados foram editados com sucesso");
                    dados = ca.listarALL();
                    this.preencheTabela(dados);
                    this.limparCampos();
                } else {
                }
            } else {
                this.limparCampos();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Localize o cargo!");
        }
    }//GEN-LAST:event_btnEditarjButton7ActionPerformed

    private void btnExcluirjButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirjButton8ActionPerformed
        boolean v = this.verificaCampos();
        if (v == true) {
            int i = JOptionPane.showConfirmDialog(null, "Deseja excluir o cargo de codigo" + lbCodigo.getText());
            if (i == 0) {

                boolean aux = ca.excluir(Integer.parseInt(lbCodigo.getText()));

                if (aux == true) {
                    JOptionPane.showMessageDialog(null, "Exclusão feita com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "O cargo esta vinculado a varios usuarios!");
                }
            } else {
            }
        } else {
            JOptionPane.showMessageDialog(null, "Localize o cargo!");
        }
        this.limparCampos();
        dados = ca.listarALL();
        this.preencheTabela(dados);
    }//GEN-LAST:event_btnExcluirjButton8ActionPerformed

